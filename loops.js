// three parts of loop
// counter - initial value
// stopping point - when to run the loop
// interation - increments the counter

for (let i = 0; i <= 10; i++) { // This is a for loop.
    console.log(`Hello this is loop number ${i}`);
}
// Create a for loop that prints numbers from 1 - 12
for(let t = 1; t <= 21; t++) {
    console.log(`Hello this is the t variable on number ${t}`)
}

// use for loop to show 12 times tables

for(var x = 1; x <= 12; x++) {
    var y = 12 * x;
    console.log(`The equation: 12 * ${x} = ${y}`);
}


